#------------------4 CASTING------------------
# - Integers
x = int(1)
y = int(2.8)
z = int("3")
print(x)
print(y)
print(z)
#Console => 1
#           2
#           3


# - Floats
x = float(1)
y = float(2.8)
z = float("3")
w = float("4.2")
print(x)
print(y)
print(z)
print(w)
#Console => 1.0
#           2.8
#           3.0
#           4.2


# - Strings
x = str("s1")
y = str(2)
z = str(3.0)
print(x)
print(y)
print(z)
#Console => s1
#           2
#           3.0