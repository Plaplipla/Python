#------------------3 NUMBERS------------------
#Verify the type of an object
x = 1
y = 2.8
z = 1j
print(type(x))
print(type(y))
print(type(z))
#Console => "<class 'int'>"
#           "<class 'float'>"
#           "<class 'complex'>"


#Create integers
x = 1
y = 3456879215
z = -3456176465
print(type(x))
print(type(y))
print(type(z))
#Console => "<class 'int'>"
#           "<class 'int'>"
#           "<class 'int'>"


#Create floating point numbers
x = 1.10
y = 1.0
z = -35.59
print(type(x))
print(type(y))
print(type(z))
#Console => "<class 'float'>"
#           "<class 'float'>"
#           "<class 'float'>"


#Create scientific numbers with an "e" to indicate the power of 10
x = 35e3
y = 12E4
z = -8707e100
print(type(x))
print(type(y))
print(type(z))
#Console => "<class 'float'>"
#           "<class 'float'>"
#           "<class 'float'>"


#Create complex numbers
x = 3+5j
y = 5j
z = -5j
print(type(x))
print(type(y))
print(type(z))
#Console => "<class 'complex'>"
#           "<class 'complex'>"
#           "<class 'complex'>"